public class UnitaryOperators {
    public static void main(String[] args) {
        int a = -5;
        int b = a; // b = (1)*a
        System.out.println("b = " + b);
        b = -a; // b = (-1)*a
        System.out.println("b = " + b);
        a = 6;
        b = +a;
        System.out.println("b = " + b);
        b = -a;
        System.out.println("b = " + b);
    }
}
